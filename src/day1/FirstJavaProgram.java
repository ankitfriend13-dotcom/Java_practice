// We can't write java program without creating a class
// When creating a new class, naming should start with initial letter in capital

public class FirstJavaProgram {
    public static void main(String[] args){             //main method
        System.out.println("My first Java program");
        System.out.print("new line");               // print vs println : println add a new line in end
        System.out.println("Same line");
    }
}

